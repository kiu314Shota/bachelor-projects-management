package com.example.demo.domain;

/**
 * Enum representing different academic years a student can be in.
 *
 * Values:
 * - FRESHMAN: First year of study (typically 1st year undergraduate).
 * - SOPHOMORE: Second year of study.
 * - JUNIOR: Third year of study.
 * - SENIOR: Fourth or final year of a standard undergraduate program.
 * - ADDITIONAL: Additional (optional or repeated) academic year.
 * - MASTER: Graduate-level (Master's degree) student.
 */
public enum YearOfStudy {
    FRESHMAN,
    SOPHOMORE,
    JUNIOR,
    SENIOR,
    ADDITIONAL,
    MASTER
}
